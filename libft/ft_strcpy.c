char	*ft_strcpy(char *dst, char *src)
{
	int		i;

	i = 0;
	while (*(src + i) != '\0')
	{
		*(dst + i) = *(src + i);
		i = i + 1;
	}
	*(dst + i) = *(src + i);
	return (dst);
}
