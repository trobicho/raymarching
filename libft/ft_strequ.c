int		ft_strequ(char *s1, char *s2)
{
	if (!s1 || !s2)
		return (0);
	while (*s1 != '\0')
	{
		if (*s2 == '\0')
			return (0);
		if (*s1 != *s2)
			return (0);
		s1 = s1 + 1;
		s2 = s2 + 1;
	}
	if (*s2 != '\0')
		return (0);
	return (1);
}
